package GameRecommendationSystem.Application;

public class Genre {
    private String genreCategory;
    private int genreCategoryId;
    private int genreId;
    private String genreName;
    private String genreDescription; 

    public String getGenreCategory() {
        return genreCategory;
    }

    // Setter method for genreCategory
    public void setGenreCategory(String genreCategory) {
        this.genreCategory = genreCategory;
    }

    // Getter method for genreCategoryId
    public int getGenreCategoryId() {
        return genreCategoryId;
    }

    // Setter method for genreCategoryId
    public void setGenreCategoryId(int genreCategoryId) {
        this.genreCategoryId = genreCategoryId;
    }

    // Getter method for genreId
    public int getGenreId() {
        return genreId;
    }

    // Setter method for genreId
    public void setGenreId(int genreId) {
        this.genreId = genreId;
    }

    // Getter method for genreName
    public String getGenreName() {
        return genreName;
    }

    // Setter method for genreName
    public void setGenreName(String genreName) {
        this.genreName = genreName;
    }

    // Getter method for genreDescription
    public String getGenreDescription() {
        return genreDescription;
    }

    // Setter method for genreDescription
    public void setGenreDescription(String genreDescription) {
        this.genreDescription = genreDescription;
    }
    

   
    
}

